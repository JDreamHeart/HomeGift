package cn.bean;

public class detail_det 
{
	private String det_text;
	private String det_pic1;
	private String det_pic2;
	private String det_pic3;
	private String det_pic4;
	public detail_det() {
		super();
	}
	public detail_det(String det_text, String det_pic1, String det_pic2,
			String det_pic3, String det_pic4) {
		super();
		this.det_text = det_text;
		this.det_pic1 = det_pic1;
		this.det_pic2 = det_pic2;
		this.det_pic3 = det_pic3;
		this.det_pic4 = det_pic4;
	}
	public String getDet_text() {
		return det_text;
	}
	public void setDet_text(String det_text) {
		this.det_text = det_text;
	}
	public String getDet_pic1() {
		return det_pic1;
	}
	public void setDet_pic1(String det_pic1) {
		this.det_pic1 = det_pic1;
	}
	public String getDet_pic2() {
		return det_pic2;
	}
	public void setDet_pic2(String det_pic2) {
		this.det_pic2 = det_pic2;
	}
	public String getDet_pic3() {
		return det_pic3;
	}
	public void setDet_pic3(String det_pic3) {
		this.det_pic3 = det_pic3;
	}
	public String getDet_pic4() {
		return det_pic4;
	}
	public void setDet_pic4(String det_pic4) {
		this.det_pic4 = det_pic4;
	}
}
